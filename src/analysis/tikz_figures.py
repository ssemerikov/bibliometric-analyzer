def emit_temporal_coupling(coupling: Dict[str, Any]) -> Path:
    eras = coupling.get("eras", {})
    labels = list(eras.keys())
    if not labels:
        return _emit("F16_temporal_coupling", "\\begin{tikzpicture}\\node{No data};\\end{tikzpicture}")
    panels = []
    for i, label in enumerate(labels):
        info = eras[label]
        n_docs = info.get("n_docs", 0)
        n_edges = info.get("n_coupling_edges", 0)
        density = info.get("density", 0)
        panels.append(
            f"\\node[draw=black!40, rounded corners=3pt, fill=blue!5, inner sep=4pt, "
            f"text width=4.4cm, align=center] (P{i}) at ({i * 5}, 0) "
            f"{{{{\\bfseries {_esc(label)}}}\\\\[2pt]"
            f"\\scriptsize $n_{{\\text{{docs}}}}={n_docs}$ \\quad "
            f"$|E|={n_edges}$ \\\\[1pt] density $={density:.4f}$}};"
        )
    body = f"""\\begin{{tikzpicture}}
{chr(10).join(panels)}
\\node[anchor=south, font=\\scriptsize\\itshape, gray] at (5, 1.2) {{Bibliographic-coupling structure stratified by era — see Table~\\ref{{tab:era-shifts}} for Kendall's $\\tau$ between consecutive eras}};
\\end{{tikzpicture}}"""
    return _emit("F16_temporal_coupling", body)


