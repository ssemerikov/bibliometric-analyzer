# ---------------------------------------------------------------------------
# F05 — Bradford zone concentration curve (Lorenz-like).
# ---------------------------------------------------------------------------

def emit_bradford_curve(bradford: Dict[str, Any]) -> Path:
    sources_sorted: List[Tuple[str, int]] = []
    for z in bradford["zones"]:
        sources_sorted.extend(z["sources"])
    if not sources_sorted:
        sources_sorted = bradford.get("top_sources", [])
    sources_sorted = sorted(sources_sorted, key=lambda x: -x[1])
    cumulative = 0
    total = sum(c for _, c in sources_sorted)
    pts = []
    for i, (_, c) in enumerate(sources_sorted, start=1):
        cumulative += c
        pts.append((i, 100.0 * cumulative / max(total, 1)))
    rows = "\n".join(f"  {i}  {p:.2f}" for i, p in pts)

    # Zone boundaries.
    cum_z = 0
    boundaries = []
    for z in bradford["zones"]:
        cum_z += z["n_sources"]
        boundaries.append(cum_z)

    bound_lines = "\n".join(
        f"\\draw[gray!50, dashed] ({{axis cs:{b},0}}) -- ({{axis cs:{b},100}}) "
        f"node[anchor=south west, font=\\scriptsize] at ({{axis cs:{b},0}}) {{Z{i+1}}};"
        for i, b in enumerate(boundaries[:-1])
    )

    body = f"""\\begin{{tikzpicture}}
\\begin{{axis}}[
  width=0.99\\linewidth, height=6.4cm,
  xlabel={{Sources, ranked by document count}},
  ylabel={{Cumulative documents (\\%)}},
  xmin=1, xmax={len(pts)}, ymin=0, ymax=100,
  axis x line=bottom, axis y line=left,
  xmode=log, log basis x=10,
  legend pos=south east,
]
\\addplot+[smooth, thick, mark=none, draw=blue!60!black]
  table[x index=0, y index=1] {{
    rank pct
{rows}
  }};
\\addlegendentry{{Concentration curve, $k={bradford.get('multiplier_k', float('nan')):.2f}$}}
{bound_lines}
\\end{{axis}}
\\end{{tikzpicture}}"""
    return _emit("F05_bradford_curve", body)


