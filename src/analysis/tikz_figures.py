def _wrap(body: str, standalone: bool = True) -> str:
    """Emit a body-only TikZ fragment.

    The fragment is meant to be ``\\input{}``-ed from the manuscript; the
    manuscript already loads the required packages (tikz, pgfplots, sansmath).
    For standalone preview, use ``test_figure_standalone.tex`` in the
    figures directory which wraps any fragment in a ``standalone`` document.
    """
    return (
        "% Auto-generated by tikz_figures.py — do not edit by hand.\n"
        + body
        + "\n"
    )


def _emit(name: str, body: str) -> Path:
    path = FIGDIR / f"{name}.tex"
    path.write_text(_wrap(body), encoding="utf-8")
    return path


def _esc(s: str) -> str:
    """LaTeX-escape a string for use in axis labels / nodes."""
    return (str(s)
            .replace("\\", r"\textbackslash{}")
            .replace("%", r"\%")
            .replace("&", r"\&")
            .replace("$", r"\$")
            .replace("#", r"\#")
            .replace("_", r"\_")
            .replace("{", r"\{")
            .replace("}", r"\}")
            .replace("^", r"\^{}")
            .replace("~", r"\~{}"))


# ---------------------------------------------------------------------------
# F01 — Annual + cumulative production timeline.
# ---------------------------------------------------------------------------

def emit_publication_timeline(by_year: pd.DataFrame, completeness: Dict[str, Any]) -> Path:
    df = by_year.sort_values("Year").reset_index(drop=True)
    rows = "\n".join(f"  {int(r.Year)}  {int(r['count'])}" for _, r in df.iterrows())

    cum = df["count"].cumsum().tolist()
    cum_rows = "\n".join(f"  {int(y)}  {int(c)}"
                         for y, c in zip(df["Year"], cum))

    # Gompertz prediction (re-fit here for the smooth curve).
    gomp = ""
    if completeness and not math.isnan(completeness.get("gompertz_r2", float("nan"))):
        from scipy.optimize import curve_fit
        years = df["Year"].astype(float).to_numpy()
        cum_arr = np.array(cum, dtype=float)
        t = years - years.min()
        try:
            K_guess = max(cum_arr[-1] * 1.5, 100.0)
            def gompertz(x, K, b, c):
                return K * np.exp(-b * np.exp(-c * x))
            p, _ = curve_fit(gompertz, t, cum_arr,
                             p0=(K_guess, 5.0, 0.2), maxfev=20000,
                             bounds=([cum_arr[-1], 0.1, 0.01], [cum_arr[-1] * 100, 50.0, 5.0]))
            ts = np.linspace(0, t.max() + 5, 80)
            ys = gompertz(ts, *p)
            yrs_smooth = years.min() + ts
            gomp_lines = "\n".join(f"  {y:.2f}  {v:.2f}" for y, v in zip(yrs_smooth, ys))
            gomp = f"""
\\addplot[smooth, thick, draw=red!70!black]
  table[x index=0, y index=1] {{
{gomp_lines}
  }};
\\addlegendentry{{Gompertz, $R^{{2}}={completeness['gompertz_r2']:.3f}$}}"""
        except Exception:
            gomp = ""

    # Bi-logistic (two-wave) prediction overlay (Meyer 1994), re-fit for the curve.
    bilo = ""
    if completeness and not math.isnan(completeness.get("bilogistic_r2", float("nan"))):
        from scipy.optimize import curve_fit
        years = df["Year"].astype(float).to_numpy()
        cum_arr = np.array(cum, dtype=float)
        t = years - years.min()
        T = float(t.max())
        try:
            def bilogistic(x, L1, k1, t1, L2, k2, t2):
                w1 = L1 / (1.0 + np.exp(-np.clip(k1 * (x - t1), -50, 50)))
                w2 = L2 / (1.0 + np.exp(-np.clip(k2 * (x - t2), -50, 50)))
                return w1 + w2
            p, _ = curve_fit(
                bilogistic, t, cum_arr,
                p0=(cum_arr[-1] * 0.5, 0.4, T * 0.45, cum_arr[-1] * 0.5, 0.4, T * 0.85),
                maxfev=40000,
                bounds=([0, 0.01, 0, 0, 0.01, 0],
                        [cum_arr[-1] * 5, 5.0, T * 1.5, cum_arr[-1] * 5, 5.0, T * 1.5]))
            ts = np.linspace(0, t.max() + 5, 80)
            ys = bilogistic(ts, *p)
            yrs_smooth = years.min() + ts
            bilo_lines = "\n".join(f"  {y:.2f}  {v:.2f}" for y, v in zip(yrs_smooth, ys))
            best = completeness.get("best_model", "")
            star = " (AIC-preferred)" if best == "bilogistic" else ""
            bilo = f"""
\\addplot[smooth, thick, dashed, draw=violet!80!black]
  table[x index=0, y index=1] {{
{bilo_lines}
  }};
\\addlegendentry{{Bi-logistic, $R^{{2}}={completeness['bilogistic_r2']:.3f}${star}}}"""
        except Exception:
            bilo = ""

    body = f"""\\begin{{tikzpicture}}
\\begin{{axis}}[
  width=0.99\\linewidth, height=6.2cm,
  ybar, bar width=4pt,
  ymin=0, axis y line*=left,
  xlabel={{Year}}, ylabel={{Annual publications}},
  enlarge x limits=0.04,
  xmin={int(df["Year"].min())-1}, xmax={int(df["Year"].max())+1},
  legend pos=north west,
]
\\addplot+[fill=blue!50!black, draw=blue!50!black, opacity=0.85]
  table[x index=0, y index=1] {{
    Year Count
{rows}
  }};
\\addlegendentry{{Annual count}}
\\end{{axis}}

\\begin{{axis}}[
  width=0.99\\linewidth, height=6.2cm,
  axis y line*=right, axis x line=none,
  ymin=0,
  ylabel={{Cumulative publications}}, ylabel near ticks,
  legend pos=south east,
]
\\addplot+[smooth, thick, mark=*, mark size=1pt, draw=teal!70!black]
  table[x index=0, y index=1] {{
{cum_rows}
  }};
\\addlegendentry{{Cumulative}}{gomp}{bilo}
\\end{{axis}}
\\end{{tikzpicture}}"""
    return _emit("F01_publication_timeline", body)


