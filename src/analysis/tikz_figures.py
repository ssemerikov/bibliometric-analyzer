# ---------------------------------------------------------------------------
# F09 — Country collaboration arc/chord (TikZ hand-positioned arc).
# ---------------------------------------------------------------------------

def emit_country_chord(affiliation: Dict[str, Any], top_countries_n: int = 12) -> Path:
    edges = affiliation.get("collaboration_edges", [])
    top = [c for c, _ in affiliation.get("top_countries", [])][:top_countries_n]
    top_set = set(top)
    edges = [e for e in edges if e["source"] in top_set and e["target"] in top_set]
    if not edges:
        body = "\\begin{tikzpicture}\\node{No collaboration edges};\\end{tikzpicture}"
        return _emit("F09_country_chord", body)

    n = len(top)
    # Position countries on a circle.
    placements = []
    for i, c in enumerate(top):
        angle = 90 - (360 * i / n)
        placements.append((c, angle))

    nodes = "\n".join(
        f"\\node[draw=black!50, fill=orange!15, rounded corners=2pt, font=\\scriptsize, "
        f"inner sep=2pt] ({_safe_id(c)}) at ({a}:4.2cm) {{{_esc(c)}}};"
        for c, a in placements
    )
    max_w = max(e["weight"] for e in edges)
    edges_lines = "\n".join(
        f"\\draw[gray!{40 + int(50 * e['weight'] / max_w)}, line width={0.3 + 1.5 * e['weight']/max_w:.2f}pt] "
        f"({_safe_id(e['source'])}.center) to[bend left=18] ({_safe_id(e['target'])}.center);"
        for e in edges
    )

    body = f"""\\begin{{tikzpicture}}[scale=1.0]
{nodes}
\\begin{{scope}}[on background layer]
{edges_lines}
\\end{{scope}}
\\node[anchor=south, font=\\scriptsize] at (0, -5.0) {{Edge weight $=$ co-authored documents (top {n} countries)}};
\\end{{tikzpicture}}"""
    return _emit("F09_country_chord", body)


def _safe_id(s: str) -> str:
    """LaTeX-safe TikZ node id."""
    return "n" + "".join(ch for ch in str(s) if ch.isalnum())[:24]


# ---------------------------------------------------------------------------
# F10 — Keyword co-occurrence network (top-N nodes).
# ---------------------------------------------------------------------------

def emit_keyword_network(keyword_network: Dict[str, Any], top_n: int = 40,
                         seed: int = 42) -> Path:
    nodes = sorted(keyword_network["nodes"], key=lambda n: -n["occurrences"])[:top_n]
    keep = {n["label"] for n in nodes}
    edges = [e for e in keyword_network["edges"]
             if e["source"] in keep and e["target"] in keep]

    G = nx.Graph()
    for n in nodes:
        G.add_node(n["label"], occurrences=n["occurrences"])
    for e in edges:
        G.add_edge(e["source"], e["target"], weight=e["weight"])

    # Spring layout, scaled to TikZ canvas.
    pos = nx.spring_layout(G, seed=seed, k=1.6, iterations=150)
    xs = [p[0] for p in pos.values()]
    ys = [p[1] for p in pos.values()]
    if not xs:
        return _emit("F10_keyword_network", "\\begin{tikzpicture}\\node{No data};\\end{tikzpicture}")
    xrange = max(max(xs) - min(xs), 0.001)
    yrange = max(max(ys) - min(ys), 0.001)
    SCALE_X = 6.5
    SCALE_Y = 5.5
    norm_pos = {k: ((v[0] - min(xs)) / xrange * SCALE_X - SCALE_X / 2,
                     (v[1] - min(ys)) / yrange * SCALE_Y - SCALE_Y / 2)
                for k, v in pos.items()}

    # Draw edges (background), then nodes labelled if occurrences high.
    max_occ = max(n["occurrences"] for n in nodes)
    max_w = max((e["weight"] for e in edges), default=1)

    edge_lines = []
    for u, v, data in G.edges(data=True):
        x1, y1 = norm_pos[u]
        x2, y2 = norm_pos[v]
        w = data.get("weight", 1)
        opacity = 0.15 + 0.6 * (w / max_w)
        lw = 0.15 + 1.0 * (w / max_w)
        edge_lines.append(f"\\draw[gray, line width={lw:.2f}pt, opacity={opacity:.2f}] "
                          f"({x1:.3f},{y1:.3f}) -- ({x2:.3f},{y2:.3f});")

    # Draw every node; then place labels with greedy collision-avoidance and a
    # radial outward offset so peripheral labels do not pile up on the dense core
    # (reviewer R2 minor #2). Labels are prioritised by occurrence then degree.
    deg = dict(G.degree())
    ranked = sorted(nodes, key=lambda n: (-n["occurrences"], -deg.get(n["label"], 0)))

    node_lines = []
    for n in nodes:
        x, y = norm_pos[n["label"]]
        size = 1.5 + 7.0 * math.sqrt(n["occurrences"] / max_occ)
        node_lines.append(
            f"\\filldraw[fill=blue!50!black, fill opacity=0.55, draw=blue!70!black, "
            f"line width=0.3pt] ({x:.3f},{y:.3f}) circle ({size:.2f}pt);"
        )

    cx = sum(p[0] for p in norm_pos.values()) / max(len(norm_pos), 1)
    cy = sum(p[1] for p in norm_pos.values()) / max(len(norm_pos), 1)
    MIN_LABEL_DIST = 0.60   # min separation between placed label anchors (cm)
    MAX_LABELS = 24
    placed = []
    label_lines = []
    for n in ranked:
        if len(placed) >= MAX_LABELS:
            break
        x, y = norm_pos[n["label"]]
        dx, dy = x - cx, y - cy
        r = math.hypot(dx, dy) or 1.0
        # Push peripheral labels further out than central ones.
        off = 0.10 + 0.12 * min(r / (SCALE_X / 2), 1.0)
        lx, ly = x + dx / r * off, y + dy / r * off
        if any(math.hypot(lx - px, ly - py) < MIN_LABEL_DIST for px, py in placed):
            continue
        placed.append((lx, ly))
        anchor = "south" if dy >= 0 else "north"
        label_lines.append(
            f"\\node[font=\\tiny, anchor={anchor}, fill=white, fill opacity=0.78, "
            f"text opacity=1, inner sep=0.5pt, rounded corners=0.5pt] "
            f"at ({lx:.3f},{ly:.3f}) {{{_esc(n['label'])}}};"
        )

    body = f"""\\begin{{tikzpicture}}[scale=1.0]
\\begin{{scope}}[on background layer]
{chr(10).join(edge_lines)}
\\end{{scope}}
{chr(10).join(node_lines)}
{chr(10).join(label_lines)}
\\end{{tikzpicture}}"""
    return _emit("F10_keyword_network", body)


# ---------------------------------------------------------------------------
# F11 — Thematic map (Callon centrality × density).
# ---------------------------------------------------------------------------

def emit_thematic_map(thematic: Dict[str, Any]) -> Path:
    clusters = thematic.get("clusters", [])
    if not clusters:
        return _emit("F11_thematic_map", "\\begin{tikzpicture}\\node{No clusters};\\end{tikzpicture}")
    cent_med = thematic.get("centrality_median", 0)
    dens_med = thematic.get("density_median", 0)
    max_n = max(c["n_nodes"] for c in clusters)

    rows = []
    labels = []
    for c in clusters:
        x = c["centrality"]
        y = c["density"]
        # Bubble size from sqrt(n_nodes) — visually proportional to cluster mass.
        size_pt = 4 + 28 * math.sqrt(c["n_nodes"] / max_n)
        label = c["top_keywords"][0][0] if c["top_keywords"] else f"C{c['cluster_id']}"
        rows.append((x, y, size_pt, label, c["quadrant"], c["n_nodes"]))
    quad_colour = {
        "motor": "red!70!black",
        "basic_transversal": "blue!70!black",
        "niche": "purple!70!black",
        "emerging_or_declining": "orange!70!black",
    }

    bubble_lines = []
    for x, y, sz, lbl, q, n_nodes in rows:
        c = quad_colour.get(q, "gray")
        bubble_lines.append(
            f"\\filldraw[fill={c}, fill opacity=0.45, draw={c}, line width=0.4pt] "
            f"(axis cs:{x:.2f},{y:.2f}) circle ({sz:.1f}pt);"
        )
        bubble_lines.append(
            f"\\node[font=\\tiny, anchor=center] at (axis cs:{x:.2f},{y:.2f}) "
            f"{{{_esc(lbl[:14])}}};"
        )

    cmax = max(c["centrality"] for c in clusters) * 1.1
    dmax = max(c["density"] for c in clusters) * 1.1

    body = f"""\\begin{{tikzpicture}}
\\begin{{axis}}[
  width=0.99\\linewidth, height=8cm,
  xlabel={{Centrality (inter-cluster link strength)}},
  ylabel={{Density (intra-cluster link strength / size)}},
  xmin=0, xmax={cmax:.2f}, ymin=0, ymax={dmax:.2f},
  axis lines=middle, axis line style={{->}},
  enlargelimits=false,
]
% Median split lines.
\\addplot[gray!50, dashed, no marks, line width=0.4pt] coordinates {{({cent_med:.2f}, 0) ({cent_med:.2f}, {dmax:.2f})}};
\\addplot[gray!50, dashed, no marks, line width=0.4pt] coordinates {{(0, {dens_med:.2f}) ({cmax:.2f}, {dens_med:.2f})}};
% Quadrant labels.
\\node[anchor=north east, font=\\scriptsize\\itshape, gray] at (axis cs:{cmax*0.99:.2f},{dmax*0.97:.2f}) {{Motor}};
\\node[anchor=north west, font=\\scriptsize\\itshape, gray] at (axis cs:{cmax*0.01:.2f},{dmax*0.97:.2f}) {{Niche}};
\\node[anchor=south east, font=\\scriptsize\\itshape, gray] at (axis cs:{cmax*0.99:.2f},{dmax*0.03:.2f}) {{Basic / transversal}};
\\node[anchor=south west, font=\\scriptsize\\itshape, gray] at (axis cs:{cmax*0.01:.2f},{dmax*0.03:.2f}) {{Emerging or declining}};
{chr(10).join(bubble_lines)}
\\end{{axis}}
\\end{{tikzpicture}}"""
    return _emit("F11_thematic_map", body)


# ---------------------------------------------------------------------------
# F12 — Three-field plot (Sources × Authors × Keywords).
# ---------------------------------------------------------------------------

def emit_three_field_plot(three: Dict[str, Any], max_per_axis: int = 8) -> Path:
    src = three.get("top_sources", [])[:max_per_axis]
    auth = three.get("top_authors", [])[:max_per_axis]
    kws = three.get("top_keywords", [])[:max_per_axis]
    sk = three.get("source_keyword_flows", [])
    ak = three.get("author_keyword_flows", [])

    if not (src and auth and kws):
        return _emit("F12_three_field", "\\begin{tikzpicture}\\node{No flows};\\end{tikzpicture}")

    # Position columns at x=0, 5, 10; y descends from top.
    H = 8.0
    def _ypos(i, n):
        return H - (i + 0.5) * H / n

    src_pos = {s: (0.0, _ypos(i, len(src))) for i, (s, _) in enumerate(src)}
    auth_pos = {a: (5.0, _ypos(i, len(auth))) for i, (a, _) in enumerate(auth)}
    kw_pos = {k: (10.0, _ypos(i, len(kws))) for i, (k, _) in enumerate(kws)}

    nodes_lines = []
    for s, c in src:
        x, y = src_pos[s]
        nodes_lines.append(
            f"\\node[draw=black!40, fill=blue!10, rounded corners=2pt, font=\\tiny, "
            f"inner sep=1.5pt, anchor=east, text width=3.5cm, align=right] "
            f"({_safe_id('S' + s)}) at ({x:.2f},{y:.2f}) {{{_esc(s[:48])} ({c})}};"
        )
    for a, c in auth:
        x, y = auth_pos[a]
        nodes_lines.append(
            f"\\node[draw=black!40, fill=teal!12, rounded corners=2pt, font=\\tiny, "
            f"inner sep=1.5pt, anchor=center, text width=2.6cm, align=center] "
            f"({_safe_id('A' + a)}) at ({x:.2f},{y:.2f}) {{{_esc(a[:32])} ({c})}};"
        )
    for k, c in kws:
        x, y = kw_pos[k]
        nodes_lines.append(
            f"\\node[draw=black!40, fill=orange!15, rounded corners=2pt, font=\\tiny, "
            f"inner sep=1.5pt, anchor=west, text width=3.5cm, align=left] "
            f"({_safe_id('K' + k)}) at ({x:.2f},{y:.2f}) {{{_esc(k[:48])} ({c})}};"
        )

    edge_lines = []
    max_w_sk = max((e["weight"] for e in sk), default=1)
    for e in sk[:25]:
        if e["source"] in src_pos and e["keyword"] in kw_pos:
            w = e["weight"]
            opa = 0.15 + 0.6 * (w / max_w_sk)
            edge_lines.append(
                f"\\draw[blue!60!black, line width={0.2 + 1.4 * w / max_w_sk:.2f}pt, "
                f"opacity={opa:.2f}] "
                f"({_safe_id('S' + e['source'])}.east) to[out=0,in=180] "
                f"({_safe_id('A' + auth[0][0])}.west);"  # routing through authors col
            )
    max_w_ak = max((e["weight"] for e in ak), default=1)
    for e in ak[:25]:
        if e["author"] in auth_pos and e["keyword"] in kw_pos:
            w = e["weight"]
            opa = 0.15 + 0.6 * (w / max_w_ak)
            edge_lines.append(
                f"\\draw[teal!70!black, line width={0.2 + 1.4 * w / max_w_ak:.2f}pt, "
                f"opacity={opa:.2f}] "
                f"({_safe_id('A' + e['author'])}.east) to[out=0,in=180] "
                f"({_safe_id('K' + e['keyword'])}.west);"
            )

    headers = (
        "\\node[anchor=south, font=\\scriptsize\\bfseries] at (0, 8.4) {Sources};"
        "\\node[anchor=south, font=\\scriptsize\\bfseries] at (5, 8.4) {Authors};"
        "\\node[anchor=south, font=\\scriptsize\\bfseries] at (10, 8.4) {Keywords};"
    )

    body = f"""\\begin{{tikzpicture}}[x=1cm, y=1cm]
{headers}
{chr(10).join(nodes_lines)}
\\begin{{scope}}[on background layer]
{chr(10).join(edge_lines)}
\\end{{scope}}
\\end{{tikzpicture}}"""
    return _emit("F12_three_field", body)


