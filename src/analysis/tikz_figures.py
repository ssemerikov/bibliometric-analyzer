# F13 — Trend topics heatmap.
# ---------------------------------------------------------------------------

def emit_trend_topics_heatmap(trend: Dict[str, Any], top_n: int = 15) -> Path:
    fb = trend.get("frequency_by_year", {})
    years = fb.get("years", [])
    keywords = fb.get("keywords", [])[:top_n]
    matrix = fb.get("matrix", [])[:top_n]
    if not (years and keywords):
        return _emit("F13_trend_topics_heatmap", "\\begin{tikzpicture}\\node{No data};\\end{tikzpicture}")

    # Restrict to years 2008..2026 for legibility, dropping the old long tail.
    keep_idx = [i for i, y in enumerate(years) if y >= 2008]
    years_k = [years[i] for i in keep_idx]
    matrix_k = [[row[i] for i in keep_idx] for row in matrix]

    max_v = max((max(r) if r else 0) for r in matrix_k)
    rows_data = []
    for ki, kw in enumerate(keywords):
        for yi, y in enumerate(years_k):
            v = matrix_k[ki][yi]
            rows_data.append((y, ki, v))

    data_block = "\n".join(f"  {y} {ki} {v}" for y, ki, v in rows_data)
    yticklabels = ",".join("{" + _esc(k) + "}" for k in keywords)
    yticks = ",".join(str(i) for i in range(len(keywords)))

    body = f"""\\begin{{tikzpicture}}
\\begin{{axis}}[
  width=0.99\\linewidth, height=9cm,
  view={{0}}{{90}}, colormap/blackwhite, point meta min=0, point meta max={max_v},
  enlargelimits=false, xlabel={{Year}}, ylabel={{}},
  ytick={{{yticks}}}, yticklabels={{{yticklabels}}},
  ymin=-0.5, ymax={len(keywords) - 0.5},
  xmin={min(years_k)-0.5}, xmax={max(years_k)+0.5},
  colorbar, colorbar style={{width=0.3cm, font=\\tiny}},
]
\\addplot3[matrix plot*, point meta=explicit, mesh/cols={len(years_k)}]
  table[meta=z] {{
    x y z
{data_block}
  }};
\\end{{axis}}
\\end{{tikzpicture}}"""
    return _emit("F13_trend_topics_heatmap", body)


