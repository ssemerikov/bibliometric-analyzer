# ---------------------------------------------------------------------------
# F07 — Lotka law fit (log-log).
# ---------------------------------------------------------------------------

def emit_lotka_fit(lotka: Dict[str, Any]) -> Path:
    dist = lotka.get("productivity_distribution", {})
    if not dist:
        body = "\\begin{tikzpicture}\\node{No data};\\end{tikzpicture}"
        return _emit("F07_lotka_fit", body)
    pts = sorted((int(k), int(v)) for k, v in dist.items())
    rows = "\n".join(f"  {n}  {f}" for n, f in pts)
    alpha = lotka.get("alpha", float("nan"))
    c = lotka.get("c", float("nan"))
    r2 = lotka.get("r_squared", float("nan"))
    # Generate fit line samples
    n_min, n_max = pts[0][0], pts[-1][0]
    fit_pts = []
    for n in np.linspace(n_min, n_max, 60):
        if n > 0:
            fit_pts.append((n, c * n ** (-alpha)))
    fit_rows = "\n".join(f"  {n:.3f}  {f:.4f}" for n, f in fit_pts if f > 0)

    body = f"""\\begin{{tikzpicture}}
\\begin{{axis}}[
  width=0.99\\linewidth, height=7cm,
  xmode=log, ymode=log, log basis x=10, log basis y=10,
  xlabel={{Publications per author $n$}},
  ylabel={{Number of authors $f(n)$}},
  legend pos=north east,
]
\\addplot+[only marks, mark=*, mark size=2pt, draw=blue!60!black, fill=blue!30]
  table[x index=0, y index=1] {{
{rows}
  }};
\\addlegendentry{{Observed}}
\\addplot+[smooth, thick, draw=red!70!black, mark=none]
  table[x index=0, y index=1] {{
{fit_rows}
  }};
\\addlegendentry{{Lotka fit, $\\alpha={alpha:.2f}$, $R^{2}={r2:.3f}$}}
\\end{{axis}}
\\end{{tikzpicture}}"""
    return _emit("F07_lotka_fit", body)


